/*
 * 
 *
 *(1)if 语句
 *		if (condition){
 *			...
 *		}
 *
 *
 *(2)if...else 语句
 *		if (condition1){
 *			...
 *		}else if (condition2){
 *			...
 *		}else{	
 *			...
 *		}
 *
 *
 *(3)switch 语句
 *		switch(n){
 *			case 1:
 *				执行代码块 1
 *				break;
 *			case 2:
 *				执行代码块 2
 *				break;
 *			default:
 *				...
 *			}
 *
 *
 *(4)三元运算符
 *			(condition)?value1:value2 
 *
 *(5)for 循环
 *		for (语句 1; 语句 2; 语句 3){
 *			被执行的代码块
 *		}
 *
 *(6)for-in 循环(遍历数组或者对象的属性)
 *		for(var i in arr){
 *			....
 *		}
 *
 *(7)while 循环
 *		while (条件){
 *			需要执行的代码
 *		}
 *
 *(8)do/while 循环
 *		do{
 *			需要执行的代码
 *		}while (条件);
 *
 *(9)break 语句用于跳出循环。（不带标签引用），只能用在循环或 switch 中
 *	
 *(10)continue 用于跳过循环中的一个迭代。（带有或不带标签引用）只能用在循环中。
 */